class ProjectAssembler:
    """Assemble generated modules into a complete standalone project.

    Creates directory structure, configs, entry point, tests, and runs
    integration tests.
    """

    def __init__(self, project_root: str):
        self.project_root = ensure_dir(project_root)

    def assemble_project(
        self,
        modules: List[GeneratedModule],
        model_spec: P2PModelSpec,
        innovation_spec: DetailedInnovationSpec,
        grid_case: str = "ieee33",
        experiment_config: Optional[Dict[str, Any]] = None,
    ) -> GeneratedProject:
        """Assemble all modules into a complete project."""
        project_dir = self.project_root
        src_dir = ensure_dir(os.path.join(project_dir, "src"))
        config_dir = ensure_dir(os.path.join(project_dir, "configs"))
        tests_dir = ensure_dir(os.path.join(project_dir, "tests"))
        outputs_dir = ensure_dir(os.path.join(project_dir, "outputs"))
        docs_dir = ensure_dir(os.path.join(project_dir, "docs"))

        grid_dir = ensure_dir(os.path.join(src_dir, "grid"))
        market_dir = ensure_dir(os.path.join(src_dir, "market"))
        algorithm_dir = ensure_dir(os.path.join(src_dir, "algorithm"))
        utils_dir = ensure_dir(os.path.join(src_dir, "utils"))

        for d in [src_dir, grid_dir, market_dir, algorithm_dir, utils_dir]:
            init_path = os.path.join(d, "__init__.py")
            if not os.path.exists(init_path):
                with open(init_path, "w", encoding="utf-8") as f:
                    f.write('"""Generated by Energy Trading Lab."""\n')

        base_modules = [m for m in modules if m.module_type in ("grid_model", "market_env")]
        algo_modules = [m for m in modules if m not in base_modules]

        module_paths = {}
        for module in modules:
            if module.module_type == "grid_model":
                target_dir = grid_dir
            elif module.module_type == "market_env":
                target_dir = market_dir
            else:
                target_dir = algorithm_dir

            file_path = os.path.join(target_dir, module.file_path)
            with open(file_path, "w", encoding="utf-8") as f:
                f.write(module.code)
            module_paths[module.module_name] = file_path

        self._write_utils(utils_dir)
        self._write_configs(config_dir, grid_case, model_spec, innovation_spec, experiment_config)
        self._write_runner(src_dir, innovation_spec, grid_case)
        self._write_requirements(project_dir)
        self._write_readme(project_dir, model_spec, innovation_spec, grid_case)
        self._write_tests(tests_dir, innovation_spec, grid_case)

        integration_passed, integration_log = self._run_integration_test(
            project_dir, innovation_spec
        )

        algo_family = innovation_spec.base_algorithm_family
        project = GeneratedProject(
            project_dir=project_dir,
            modules=modules,
            algorithm_family=algo_family,
            innovation_mode=innovation_spec.innovation_mode,
            integration_test_passed=integration_passed,
            integration_test_log=integration_log,
        )

        return project

