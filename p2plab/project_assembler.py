    def _run_integration_test(
        self,
        project_dir: str,
        innovation_spec: DetailedInnovationSpec,
    ) -> tuple[bool, str]:
        """Run integration smoke test on the assembled project."""
        project_dir = os.path.abspath(project_dir)
        runner_path = os.path.join(project_dir, "src", "run_experiment.py")
        config_path = os.path.join(project_dir, "configs", "smoke_config.json")
        output_dir = os.path.join(project_dir, "outputs", "integration_test")

        if not os.path.exists(runner_path):
            return False, "Runner script not found"

        try:
            result = subprocess.run(
                [sys.executable, runner_path, "--config", config_path, "--output-dir", output_dir, "--quiet"],
                cwd=project_dir,
                capture_output=True,
                text=True,
                timeout=60,
                env={**os.environ, "PYTHONPATH": os.path.join(project_dir, "src") + os.pathsep + os.environ.get("PYTHONPATH", "")},
            )
            log = f"STDOUT:\n{result.stdout}\n\nSTDERR:\n{result.stderr}"
            if result.returncode != 0:
                return False, log
            return True, log
        except subprocess.TimeoutExpired:
            return False, "Integration test timed out"
        except Exception as e:
            return False, f"Integration test error: {str(e)}"
